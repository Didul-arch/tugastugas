import os
import cv2
import numpy as np
import math
import matplotlib.pyplot as plt

output_dir = './laporan/assets/output'
os.makedirs(output_dir, exist_ok=True)

base_img_lines = cv2.imread('./laporan/assets/cubes.jpeg')
gray_lines = cv2.cvtColor(base_img_lines, cv2.COLOR_BGR2GRAY)
edges_lines = cv2.Canny(gray_lines, 200, 220, apertureSize=3)

def draw_hough_lines(img, lines, color):
    if lines is None:
        return

    for rho, theta in lines[:, 0, :]:
        a = math.cos(theta)
        b = math.sin(theta)
        x0 = a * rho
        y0 = b * rho
        x1 = int(x0 + 1000 * (-b))
        y1 = int(y0 + 1000 * (a))
        x2 = int(x0 - 1000 * (-b))
        y2 = int(y0 - 1000 * (a))
        cv2.line(img, (x1, y1), (x2, y2), color, 2)

def gridtest_hough_lines(
    values,
    detector,
    title,
    file_prefix,
    parameter_name,
    label_text_fn=str,
    label_file_fn=str,
    line_color=(0, 255, 255),
):
    fig, axes = plt.subplots(1, len(values), figsize=(6 * len(values), 6))
    axes = np.atleast_1d(axes)

    for idx, value in enumerate(values):
        img = base_img_lines.copy()
        lines = detector(value)
        draw_hough_lines(img, lines, line_color)

        label_text = label_text_fn(value)
        label_file = label_file_fn(value)
        cv2.imwrite(f'{output_dir}/{file_prefix}_{label_file}.png', img)
        axes[idx].imshow(cv2.cvtColor(img, cv2.COLOR_BGR2RGB))
        axes[idx].set_title(f'{parameter_name} = {label_text}')
        axes[idx].axis('off')

    plt.suptitle(title, fontsize=14)
    plt.tight_layout()
    plt.savefig(f'{output_dir}/{file_prefix}_grid.png', dpi=200, bbox_inches='tight')
    plt.show()

rho_values = [1, 2, 4]
