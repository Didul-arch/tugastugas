base_img_circles = cv2.imread('./laporan/assets/citrus.jpeg')
gray_circles = cv2.cvtColor(base_img_circles, cv2.COLOR_BGR2GRAY)
gray_circles = cv2.medianBlur(gray_circles, 11)

def draw_hough_circles(img, circles, outer_color, center_color):
    if circles is None:
        return

    circles = np.uint16(np.around(circles))
    for circle in circles[0, :]:
        cv2.circle(img, (circle[0], circle[1]), circle[2], outer_color, 2)
        cv2.circle(img, (circle[0], circle[1]), 2, center_color, 3)

def gridtest_hough_circles(
    values,
    detector,
    title,
    file_prefix,
    parameter_name,
    label_text_fn=str,
    label_file_fn=str,
    outer_color=(0, 255, 0),
    center_color=(0, 0, 255),
):
    fig, axes = plt.subplots(1, len(values), figsize=(6 * len(values), 6))
    axes = np.atleast_1d(axes)

    for idx, value in enumerate(values):
        img = base_img_circles.copy()
        circles = detector(value)
        draw_hough_circles(img, circles, outer_color, center_color)

        label_text = label_text_fn(value)
        label_file = label_file_fn(value)
        cv2.imwrite(f'{output_dir}/{file_prefix}_{label_file}.png', img)
        axes[idx].imshow(cv2.cvtColor(img, cv2.COLOR_BGR2RGB))
        axes[idx].set_title(f'{parameter_name} = {label_text}')
        axes[idx].axis('off')

    plt.suptitle(title, fontsize=14)
    plt.tight_layout()
    plt.savefig(f'{output_dir}/{file_prefix}_grid.png', dpi=200, bbox_inches='tight')
    plt.show()

